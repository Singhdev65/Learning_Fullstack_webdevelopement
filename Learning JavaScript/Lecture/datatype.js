/* Data Types in JavaScript


numbers => integer or floating-point
string => A string may have zero or more characters
boolean => true/false
null => unknown values
undefined => unassigned values
Objects => objects are used to store collections of 
            data and more complex entities.
Symbols => unique identifiers


typeof operator/function returns the type of arguments.

it supports two forms of argument.

1. As an operator => typeof x
2. As an function => typeof(x)

Examples:

typeof undefined // "undefined"

typeof 0 => "number"

typeof true => "boolean"

typeof "foo" => "string"

typeof Symbol("id") => "symbol"

typeof Math => "object"

typeof null  =>"object"

typeof alert => "function"


*/